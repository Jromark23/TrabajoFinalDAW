	<h1 class="politica__heading">Política de Cookies</h1>

	<p class="politica__texto">
		En <strong>Proyecto Joel</strong> (en adelante, “<em>nosotros</em>” o “<em>nuestro sitio</em>”), utilizamos
		cookies y tecnologías similares para que este sitio web funcione correctamente, mejore tu experiencia
		como usuario y nos permita analizar el tráfico y uso de la página. Esta política de cookies explica
		qué son las cookies, cómo las utilizamos, qué tipos empleamos y cómo puedes configurar o
		rechazar su uso.
	</p>

	<h2 class="politica__subtitulo">1. ¿Qué son las cookies?</h2>
	<p class="politica__texto">
		Las cookies son ficheros de texto que se descargan en tu ordenador, tableta, móvil u otro dispositivo
		cuando visitas una página web. Contienen información que se transfiere al disco duro de tu dispositivo.
		Su principal finalidad es hacer que los sitios web funcionen, o funcionen de forma más eficiente, así
		como proporcionar información a los propietarios del sitio.
	</p>

	<h2 class="politica__subtitulo">2. Tipos de cookies que utilizamos</h2>

	<h3 class="politica__categoria">2.1. Cookies estrictamente necesarias (técnicas)</h3>
	<p class="politica__texto">
		Estas cookies son esenciales para permitirte moverte por el sitio web y utilizar sus funcionalidades
		básicas, como acceder a áreas seguras de la página. Sin estas cookies, servicios como el carrito de
		la compra o la autenticación no funcionarían correctamente.
	</p>

	<h3 class="politica__categoria">2.2. Cookies de rendimiento y análisis (analíticas)</h3>
	<p class="politica__texto">
		Recogen información sobre cómo accedes y usas nuestro sitio web. No identifican al usuario. Se usa para mejorar el funcionamiento.
	</p>

	<h3 class="politica__categoria">2.3. Cookies de publicidad comportamental</h3>
	<p class="politica__texto">
		Almacenan información sobre tus hábitos de navegación para mostrar contenido o anuncios personalizados.
	</p>

	<h3 class="politica__categoria">2.4. Cookies de terceros</h3>
	<p class="politica__texto">
		Algunas cookies son gestionadas por terceros para generar estadísticas o publicidad personalizada en otros sitios.
	</p>

